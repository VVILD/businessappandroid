public class Billing_Product {
    private String applied_weight;
    private String cod_cost;
    private String date;
    private String name;
    private String price;
    private String quantity;
    private String remittance;
    private String return_cost;
    private String shipping_cost;
    private String tracking_id;

    public String getApplied_weight() {
        return applied_weight;
    }

    public void setApplied_weight(String applied_weight) {
        this.applied_weight = applied_weight;
    }

    public String getCod_cost() {
        return cod_cost;
    }

    public void setCod_cost(String cod_cost) {
        this.cod_cost = cod_cost;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getRemittance() {
        return remittance;
    }

    public void setRemittance(String remittance) {
        this.remittance = remittance;
    }

    public String getReturn_cost() {
        return return_cost;
    }

    public void setReturn_cost(String return_cost) {
        this.return_cost = return_cost;
    }

    public String getShipping_cost() {
        return shipping_cost;
    }

    public void setShipping_cost(String shipping_cost) {
        this.shipping_cost = shipping_cost;
    }

    public String getTracking_id() {
        return tracking_id;
    }

    public void setTracking_id(String tracking_id) {
        this.tracking_id = tracking_id;
    }
}
